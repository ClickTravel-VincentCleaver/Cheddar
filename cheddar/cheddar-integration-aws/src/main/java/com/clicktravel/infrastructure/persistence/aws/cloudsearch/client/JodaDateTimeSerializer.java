package com.clicktravel.infrastructure.persistence.aws.cloudsearch.client;

import java.io.IOException;

import org.joda.time.DateTime;
import org.joda.time.format.DateTimeFormatter;
import org.joda.time.format.ISODateTimeFormat;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;

public class JodaDateTimeSerializer extends JsonSerializer<DateTime> {

    private static final DateTimeFormatter formatter = ISODateTimeFormat.dateTime().withZoneUTC();

    @Override
    public void serialize(final DateTime value, final JsonGenerator jgen, final SerializerProvider provider)
            throws IOException, JsonProcessingException {
        jgen.writeString(formatter.print(value));
    }

}
